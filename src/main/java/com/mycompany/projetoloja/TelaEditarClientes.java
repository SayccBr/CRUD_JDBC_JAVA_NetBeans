    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTEditCli.getModel();
        modelo.setNumRows(0);
        LojaOnline loja = new LojaOnline();
        
        for(Cliente c: loja.read()){
            
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getEndereco()
            });
        }
    }

