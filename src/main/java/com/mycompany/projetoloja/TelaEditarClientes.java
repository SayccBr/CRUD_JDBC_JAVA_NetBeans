    private void jTEditCliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTEditCliMouseClicked
       if(jTEditCli.getSelectedRow() != -1){
            
            tfNome.setText(jTEditCli.getValueAt(jTEditCli.getSelectedRow(), 1).toString());
            tfEmail.setText(jTEditCli.getValueAt(jTEditCli.getSelectedRow(), 2).toString());
            tfEndereco.setText(jTEditCli.getValueAt(jTEditCli.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_jTEditCliMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTEditCli.getSelectedRow() != -1){
        
            Cliente cli = new Cliente();
            LojaOnline loja = new LojaOnline();

            cli.setNome(tfNome.getText());
            cli.setEmail(tfEmail.getText());
            cli.setEndereco(tfEndereco.getText());
            cli.setId((int)jTEditCli.getValueAt(jTEditCli.getSelectedRow(), 0));

            loja.ClienteUpdate(cli);

            tfNome.setText("");
            tfEmail.setText("");
            tfEndereco.setText("");
            
            readJTable();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

